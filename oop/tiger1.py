## This is the simplest OOP script possible
## Note that, there is no use of ```__init__``` method


class Tiger:
  
  
  def can_run(self):
    print("Tiger can run")
    
    
  def can_eat(self):
    print("Tiger can eat")
    
    
    
def main():
	x= Tiger()
	x.can_run()
	x.can_eat()   



if __name__ == "__main__":
	main()
  
  
