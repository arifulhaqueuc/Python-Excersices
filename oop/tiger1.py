## This is the simplest OOP script possible
## Note that, there is no use of ```__init__``` method


class Tiger:
  
  
  def run(self):
    print("Tiger can run")
    
    
  def eat(self):
    print("Tiger can eat")
    
    
    
def main():
	x= Tiger()
	x.run()
	x.eat()   



if __name__ == "__main__":
	main()
  
  
